<h3>Summary</h3>

<p>Append is an open-source website builder dedicated to powering a freer, faster, and more scalable internet.</p>

<h3>How To Use</h3>

<p>The goal is to make Append as intuitive as possible. Simply drag, drop, point, and click to create complex and modern websites.</p>

<p>However, building things that are easy to use can be hard.</p>

<p>So detailed guides and step-by-step tutorials should be made. Hopefully soon!</p>

<h3>About Append</h3>

<p>Twitter: <a class="link" href="https://twitter.com/appendweb3" rel="external noopener nofollow" target="_blank">@appendweb3</a></p>
<p>GitHub: <a class="link" href="https://github.com/natclark/append" rel="external noopener nofollow" target="_blank">natclark/append</a></p>

<p>Hey, I'm Nathan! I'm a long-time Web 3.0 developer and decentralization enthusiast.</p>

<p>I'm the creator and current maintainer of Append. As of writing this, the project is in a very early stage. If the project gets traction, which I hope it does, then governance will be decentralized through a DAO or DAC.</p>

<p>A couple years back, I started working on a decentralized search engine for blockchain domains. I don't think the market was ready, and I dropped the ball.</p>

<p>But it's definitely matured a lot since then. Things have moved quickly this past year.</p>

<p>And the market is more than ready for a drag-and-drop website builder for decentralized, unstoppable websites.</p>

<p>So, I built a prototype of one, and I'm working around the clock to get the ball rolling and bring it to the masses!</p>

